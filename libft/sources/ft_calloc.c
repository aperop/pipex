/*
Функция ft_calloc выделяет (с помощью malloc)
и возвращает «свежую» область памяти.
Выделенная память инициализируется в 0.
Если распределение не удается, функция возвращает NULL.
*/
#include "../includes/libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;

	ptr = malloc(count * size);
	if (ptr == 0)
		return (ptr);
	ft_bzero(ptr, count * size);
	return (ptr);
}
