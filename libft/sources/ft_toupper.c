/*
Функция toupper возвращает соответствующий верхнему регистру
эквивалент символа c, если c — это буква.
В противном случае c возвращается неизмененным.
*/
#include "../includes/libft.h"

int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
		return (c + 'A' - 'a');
	return (c);
}
